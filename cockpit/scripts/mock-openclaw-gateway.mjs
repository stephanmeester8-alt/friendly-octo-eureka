#!/usr/bin/env node
/**
 * Mock OpenClaw Gateway for local E2E testing.
 * Honors tool.execution.release / tool.execution.abort from the cockpit proxy.
 */

import http from "node:http";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { WebSocketServer } from "ws";

const PORT = Number(process.env.MOCK_GATEWAY_PORT ?? 18789);
const HOST = process.env.MOCK_GATEWAY_HOST ?? "127.0.0.1";
const WORKSPACE_ROOT = path.resolve(
  process.env.WORKSPACE_ROOT ??
    path.join(path.dirname(fileURLToPath(import.meta.url)), "../../workspace"),
);

/** @type {Map<string, { socket: import("ws").WebSocket, sessionKey: string, projectSlug: string, stage: string }>} */
const pendingRuns = new Map();

function send(socket, frame) {
  socket.send(JSON.stringify(frame));
}

function agentEvent(runId, stream, data, extra = {}) {
  return {
    type: "event",
    event: "agent",
    payload: { runId, stream, seq: Date.now(), ts: Date.now(), data, ...extra },
  };
}

function delay(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function writeWorkflowArtifacts(projectSlug) {
  const projectDir = path.join(WORKSPACE_ROOT, "projects", projectSlug);
  const srcPath = path.join(projectDir, "src", "cli_tool.py");
  const docsPath = path.join(projectDir, "docs", "CLI_SPEC.md");

  await fs.mkdir(path.dirname(srcPath), { recursive: true });
  await fs.mkdir(path.dirname(docsPath), { recursive: true });

  await fs.writeFile(
    srcPath,
    `#!/usr/bin/env python3
"""E2E generated CLI tool."""

def greet(name: str) -> str:
    return f"Hello, {name}!"

if __name__ == "__main__":
    print(greet("E2E"))
`,
    "utf-8",
  );

  await fs.writeFile(
    docsPath,
    `# CLI Tool Specification

Generated by the E2E workflow test.

## Commands

- \`greet <name>\` — print a greeting
`,
    "utf-8",
  );

  return { srcPath, docsPath };
}

async function runScenario(socket, params, requestId) {
  const runId = crypto.randomUUID();
  const sessionKey = String(params.sessionKey ?? "");
  const projectSlug = sessionKey.replace(/^cockpit:project:/, "") || "demo-project";
  const model = String(params.model ?? "default");

  pendingRuns.set(runId, {
    socket,
    sessionKey,
    projectSlug,
    stage: "started",
  });

  send(socket, {
    type: "res",
    id: requestId,
    ok: true,
    payload: { runId, status: "accepted", model },
  });

  const base = { runId, sessionKey };

  send(
    socket,
    agentEvent(
      runId,
      "assistant",
      {
        type: "text",
        text: `Planning Python CLI scaffold under src/ and docs/ (model: ${model})…`,
      },
      base,
    ),
  );

  await delay(300);

  send(
    socket,
    agentEvent(
      runId,
      "tool",
      {
        name: "terminal_exec",
        phase: "start",
        executionId: runId,
        input: { command: "python -m pytest" },
      },
      base,
    ),
  );

  pendingRuns.set(runId, {
    socket,
    sessionKey,
    projectSlug,
    stage: "awaiting_proxy_release",
  });
}

async function continueAfterRelease(runId) {
  const run = pendingRuns.get(runId);
  if (!run || run.stage !== "awaiting_proxy_release") {
    return false;
  }

  const { socket, sessionKey, projectSlug } = run;
  const base = { runId, sessionKey };

  send(
    socket,
    agentEvent(
      runId,
      "tool",
      {
        name: "file_write",
        phase: "start",
        input: { path: `${projectSlug}/src/cli_tool.py` },
      },
      base,
    ),
  );

  await writeWorkflowArtifacts(projectSlug);

  send(
    socket,
    agentEvent(
      runId,
      "tool",
      {
        name: "file_write",
        phase: "result",
        input: { path: `${projectSlug}/src/cli_tool.py` },
        output: { bytesWritten: 128 },
      },
      base,
    ),
  );

  send(
    socket,
    agentEvent(runId, "assistant", {
      type: "text",
      text: "CLI tool and docs written successfully.",
    }, base),
  );

  pendingRuns.delete(runId);
  return true;
}

async function abortRun(runId) {
  const run = pendingRuns.get(runId);
  if (!run) {
    return false;
  }

  const { socket, sessionKey } = run;
  send(
    socket,
    agentEvent(
      runId,
      "error",
      { message: "Operator rejected terminal_exec" },
      { runId, sessionKey },
    ),
  );
  pendingRuns.delete(runId);
  return true;
}

const server = http.createServer(async (req, res) => {
  if (req.method === "GET" && req.url === "/health") {
    res.writeHead(200, { "content-type": "application/json" });
    res.end(JSON.stringify({ ok: true, service: "mock-openclaw-gateway" }));
    return;
  }

  if (req.method === "GET" && req.url === "/proxy/state") {
    const suspended = [...pendingRuns.entries()]
      .filter(([, run]) => run.stage === "awaiting_proxy_release")
      .map(([runId, run]) => ({
        executionId: runId,
        runId,
        projectSlug: run.projectSlug,
        stage: run.stage,
      }));
    res.writeHead(200, { "content-type": "application/json" });
    res.end(JSON.stringify({ suspended }));
    return;
  }

  res.writeHead(404);
  res.end("Not found");
});

const wss = new WebSocketServer({ server });

wss.on("connection", (socket) => {
  send(socket, {
    type: "event",
    event: "connect.challenge",
    payload: { nonce: crypto.randomUUID(), ts: Date.now() },
  });

  socket.on("message", (raw) => {
    let frame;
    try {
      frame = JSON.parse(String(raw));
    } catch {
      return;
    }

    if (frame.type === "req" && frame.method === "connect") {
      send(socket, {
        type: "res",
        id: frame.id,
        ok: true,
        payload: {
          type: "hello-ok",
          protocol: 4,
          server: { version: "mock-0.2.0", connId: crypto.randomUUID() },
          features: {
            methods: ["agent", "tool.execution.release", "tool.execution.abort"],
            events: ["agent"],
          },
          snapshot: {},
          policy: {
            maxPayload: 26214400,
            maxBufferedBytes: 52428800,
            tickIntervalMs: 15000,
          },
          auth: {
            role: "operator",
            scopes: ["operator.read", "operator.write", "operator.approvals"],
          },
        },
      });
      return;
    }

    if (frame.type === "req" && frame.method === "agent") {
      void runScenario(socket, frame.params ?? {}, frame.id);
      return;
    }

    if (frame.type === "req" && frame.method === "tool.execution.release") {
      const executionId = String(
        frame.params?.executionId ?? frame.params?.runId ?? "",
      );
      const targetRunId =
        executionId && pendingRuns.has(executionId)
          ? executionId
          : [...pendingRuns.entries()].find(
              ([, run]) => run.stage === "awaiting_proxy_release",
            )?.[0];
      void continueAfterRelease(targetRunId ?? "").then((released) => {
        send(socket, {
          type: "res",
          id: frame.id,
          ok: released,
          payload: { released, executionId },
          ...(released ? {} : { error: { message: `No suspended run: ${executionId}` } }),
        });
      });
      return;
    }

    if (frame.type === "req" && frame.method === "tool.execution.abort") {
      const executionId = String(
        frame.params?.executionId ?? frame.params?.runId ?? "",
      );
      void abortRun(executionId).then((aborted) => {
        send(socket, {
          type: "res",
          id: frame.id,
          ok: aborted,
          payload: { aborted, executionId },
          ...(aborted ? {} : { error: { message: `No suspended run: ${executionId}` } }),
        });
      });
      return;
    }

    if (frame.type === "req") {
      send(socket, {
        type: "res",
        id: frame.id,
        ok: true,
        payload: {},
      });
    }
  });
});

server.listen(PORT, HOST, () => {
  console.log(`[mock-gateway] listening on http://${HOST}:${PORT}`);
  console.log(`[mock-gateway] workspace root: ${WORKSPACE_ROOT}`);
});
