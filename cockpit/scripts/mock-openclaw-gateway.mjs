#!/usr/bin/env node
/**
 * Minimal OpenClaw gateway mock for local E2E workflow tests.
 * Implements WS handshake, agent RPC ack, streamed tool events, and approval.resolve.
 */

import http from "node:http";
import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { WebSocketServer } from "ws";

const PORT = Number(process.env.MOCK_GATEWAY_PORT ?? 18789);
const HOST = process.env.MOCK_GATEWAY_HOST ?? "127.0.0.1";
const WORKSPACE_ROOT = path.resolve(
  process.env.WORKSPACE_ROOT ??
    path.join(path.dirname(fileURLToPath(import.meta.url)), "../../workspace"),
);

const pendingRuns = new Map();

function send(socket, frame) {
  socket.send(JSON.stringify(frame));
}

function agentEvent(runId, stream, data, extra = {}) {
  return {
    type: "event",
    event: "agent",
    payload: { runId, stream, seq: Date.now(), ts: Date.now(), data, ...extra },
  };
}

async function writeWorkflowArtifacts(projectSlug) {
  const projectDir = path.join(WORKSPACE_ROOT, "projects", projectSlug);
  const srcPath = path.join(projectDir, "src", "cli_tool.py");
  const docsPath = path.join(projectDir, "docs", "CLI_SPEC.md");

  await fs.mkdir(path.dirname(srcPath), { recursive: true });
  await fs.mkdir(path.dirname(docsPath), { recursive: true });

  await fs.writeFile(
    srcPath,
    `#!/usr/bin/env python3
"""E2E generated CLI tool."""

def greet(name: str) -> str:
    return f"Hello, {name}!"

if __name__ == "__main__":
    print(greet("E2E"))
`,
    "utf-8",
  );

  await fs.writeFile(
    docsPath,
    `# CLI Tool Specification

Generated by the E2E workflow test.

## Commands

- \`greet <name>\` — print a greeting
`,
    "utf-8",
  );

  return { srcPath, docsPath };
}

async function runScenario(socket, params, requestId) {
  const runId = crypto.randomUUID();
  const sessionKey = String(params.sessionKey ?? "");
  const projectSlug = sessionKey.replace(/^cockpit:project:/, "") || "demo-project";

  pendingRuns.set(runId, { socket, sessionKey, projectSlug, stage: "started" });

  send(socket, {
    type: "res",
    id: requestId,
    ok: true,
    payload: { runId, status: "accepted" },
  });

  const base = { runId, sessionKey };

  send(
    socket,
    agentEvent(runId, "assistant", {
      type: "text",
      text: "Planning Python CLI scaffold under src/ and docs/…",
    }, base),
  );

  await delay(300);

  send(
    socket,
    agentEvent(
      runId,
      "tool",
      {
        name: "terminal_exec",
        phase: "start",
        input: { command: "python -m pytest" },
      },
      base,
    ),
  );

  pendingRuns.set(runId, {
    socket,
    sessionKey,
    projectSlug,
    stage: "awaiting_approval",
  });
}

async function continueAfterApproval(runId, approved) {
  const run = pendingRuns.get(runId);
  if (!run || run.stage !== "awaiting_approval") {
    return;
  }

  const { socket, sessionKey, projectSlug } = run;
  const base = { runId, sessionKey };

  if (!approved) {
    send(
      socket,
      agentEvent(runId, "error", {
        message: "Operator rejected terminal_exec",
      }, base),
    );
    pendingRuns.delete(runId);
    return;
  }

  send(
    socket,
    agentEvent(
      runId,
      "tool",
      {
        name: "file_write",
        phase: "start",
        input: { path: `${projectSlug}/src/cli_tool.py` },
      },
      base,
    ),
  );

  await writeWorkflowArtifacts(projectSlug);

  send(
    socket,
    agentEvent(
      runId,
      "tool",
      {
        name: "file_write",
        phase: "result",
        input: { path: `${projectSlug}/src/cli_tool.py` },
        output: { bytesWritten: 128 },
      },
      base,
    ),
  );

  send(
    socket,
    agentEvent(runId, "assistant", {
      type: "text",
      text: "CLI tool and docs written successfully.",
    }, base),
  );

  send(socket, {
    type: "res",
    id: crypto.randomUUID(),
    ok: true,
    payload: { runId, status: "ok", summary: "completed" },
  });

  pendingRuns.delete(runId);
}

function delay(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

const server = http.createServer(async (req, res) => {
  if (req.method === "POST" && req.url === "/mock/resume") {
    let body = "";
    for await (const chunk of req) {
      body += chunk;
    }
    const payload = body ? JSON.parse(body) : {};
    const runId = payload.runId;
    const entry = runId
      ? pendingRuns.has(runId)
        ? [runId, pendingRuns.get(runId)]
        : null
      : [...pendingRuns.entries()].find(([, run]) => run.stage === "awaiting_approval");

    if (!entry || !entry[1]) {
      res.writeHead(404, { "Content-Type": "application/json" });
      res.end(JSON.stringify({ error: "No awaiting run" }));
      return;
    }

    const resolvedRunId = runId ?? entry[0];
    res.writeHead(200, { "Content-Type": "application/json" });
    res.end(JSON.stringify({ ok: true, runId: resolvedRunId }));
    void continueAfterApproval(resolvedRunId, true);
    return;
  }

  res.writeHead(200, { "Content-Type": "text/plain" });
  res.end("mock-openclaw-gateway\n");
});

const wss = new WebSocketServer({ server });

wss.on("connection", (socket) => {
  send(socket, {
    type: "event",
    event: "connect.challenge",
    payload: { nonce: crypto.randomUUID(), ts: Date.now() },
  });

  socket.on("message", (raw) => {
    let frame;
    try {
      frame = JSON.parse(String(raw));
    } catch {
      return;
    }

    if (frame.type === "req" && frame.method === "connect") {
      send(socket, {
        type: "res",
        id: frame.id,
        ok: true,
        payload: {
          type: "hello-ok",
          protocol: 4,
          server: { version: "mock-0.1.0", connId: crypto.randomUUID() },
          features: { methods: ["agent", "approval.resolve"], events: ["agent"] },
          snapshot: {},
          policy: {
            maxPayload: 26214400,
            maxBufferedBytes: 52428800,
            tickIntervalMs: 15000,
          },
          auth: {
            role: "operator",
            scopes: ["operator.read", "operator.write", "operator.approvals"],
          },
        },
      });
      return;
    }

    if (frame.type === "req" && frame.method === "agent") {
      void runScenario(socket, frame.params ?? {}, frame.id);
      return;
    }

    if (frame.type === "req" && frame.method === "approval.resolve") {
      const approved = frame.params?.decision === "approve";
      const approvalId = String(frame.params?.id ?? "");
      const runEntry = [...pendingRuns.entries()].find(([, run]) =>
        run.stage === "awaiting_approval",
      );

      send(socket, {
        type: "res",
        id: frame.id,
        ok: true,
        payload: { id: approvalId, decision: frame.params?.decision },
      });

      if (runEntry) {
        void continueAfterApproval(runEntry[0], approved);
      }
      return;
    }

    if (frame.type === "req") {
      send(socket, {
        type: "res",
        id: frame.id,
        ok: true,
        payload: {},
      });
    }
  });
});

server.listen(PORT, HOST, () => {
  console.log(`[mock-gateway] listening on http://${HOST}:${PORT}`);
  console.log(`[mock-gateway] workspace root: ${WORKSPACE_ROOT}`);
});
